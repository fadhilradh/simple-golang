package itung


func Tambah(num1, num2 int) int {
	result := num1 + num2
	return result
}

func Kurangi(num1, num2 int) int {
	result := num1 - num2
	return result
}

func Kali(num1, num2 int) int {
	result := num1 * num2
	return result
}

func Bagi(num1, num2 int) int {
	result := num1 - num2
	return result
}